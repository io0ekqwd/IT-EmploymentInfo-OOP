package data;

public class InterviewDetails {
	
	//Attributes of interview details
	private String venue;
	private int day;
	private String month;
	private int year;
	private int hour;
	private int min;
	
	//Getter and setters for venue
	public String getVenue() {
	 	 return venue; 
	}
	public void setVenue(String venue) { 
		 this.venue = venue; 
	}
	//Getter and setters for hour
	public int getHour() {
	 	 return hour; 
	}
	public void setHour(int hour) { 
		 this.hour = hour; 
	}
	//Getter and setters for min
	public int getMin() {
	 	 return min; 
	}
	public void setMin(int min) { 
		 this.min = min; 
	}
	//Getter and setters for day
	public int getDay() {
	 	 return day; 
	}
	public void setDay(int day) { 
		 this.day = day; 
	}
	//Getter and setters for month
	public String getMonth() {
	 	 return month; 
	}
	public void setMonth(String month) { 
		 this.month = month; 
	}
	//Getter and setters for year
	public int getYear() {
	 	 return year; 
	}
	public void setYear(int year) { 
		 this.year= year; 
	}

}
